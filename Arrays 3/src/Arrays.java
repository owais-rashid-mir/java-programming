//Printing elements of an array- Third way

public class Arrays {
	public static void main(String[] args) {
		
		int[] arr1 = {10,20,30};
		
		for(int i=0 ; i<3 ; i++)
		{
			System.out.println(arr1[i]);
		}
		
		System.out.println();
// ----------------------------------------------------------------------------------------
		int[] arr2 = {40,50,60};
		
		for(int i=0 ; i<arr2.length ; i++)
		{
			System.out.println(arr2[i]);
		}
		
	}

}
