//Whenever a String array is declared but not initialized, the output is 'null' (without quotes). 
public class aos {
	public static void main(String[] args) {

		String[] words = new String[2];
		
		System.out.println(words[0]);
		System.out.println(words[1]);
		
		
//Whenever a integer array is declared but not initialized, the output is 0.		
		int[] arr = new int[2];
		
		System.out.println(arr[0]);
		System.out.println(arr[1]);
		
	}
}
